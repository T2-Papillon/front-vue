                    <!-- <SortFilter :sortByLatest="() => sortByLatest(doingProjects)" :sortByPriority="() => sortByPriority(doingProjects)" />
                    </div> -->
                </div>
                <ProjectTable v-if="doingProjects.length > 0" :projects="doingProjects" />
                <p v-else>프로젝트가 없습니다.</p>
            </div>
        </section>

